public class Operator 
{
    public static void main(String[] args)
    {
        double soduong = 9.5, soam = -9.6;
        System.out.println(-soduong);
        System.out.println(+soam);
        System.out.println(++soduong);
        System.out.println(--soduong);
        System.out.println(soduong++);
        System.out.println(soduong--);
        System.out.println(soduong);
        boolean isValid = false;
        System.out.println(!isValid);
    }
}
